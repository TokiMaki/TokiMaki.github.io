def build_black_fang_recommendation_row(
    slot: str,
    item_id: str,
    item_name: str,
    item_rarity: str,
    icon_url: str,
    item_explain: str,
    effects: dict,
    current_effects: dict,
    target_effects: dict,
    auction: dict,
    materials: list,
    material_text: str,
    target_item_name: str,
) -> dict:
    return {
        "slot": slot,
        "tier": "흑아",
        "itemId": item_id,
        "itemName": item_name,
        "itemRarity": item_rarity,
        "iconUrl": icon_url,
        "itemExplain": item_explain,
        "effects": effects,
        "currentEffects": current_effects,
        "targetEffects": target_effects,
        "auction": auction,
        "expectedGold": auction.get("minUnitPrice"),
        "materials": materials,
        "materialText": material_text,
        "targetItemName": target_item_name,
    }
